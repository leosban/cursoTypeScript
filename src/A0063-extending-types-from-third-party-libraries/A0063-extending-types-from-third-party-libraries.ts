import _ from './module';

// Array de exemplo
const array = [100, 200, 300, 400, 500];

// Metodo soma do array com lodash
console.log(_.sum(array));

// Metodo menor numero do array com lodash
console.log(_.min(array));

// Metodo maior numero do array com lodash
console.log(_.max(array));

// Metodo media da soma dos numero do array com lodash
console.log(_.mean(array));

// Metodo multiplicação dos números do array com lodash
console.log(_.mul(array));

